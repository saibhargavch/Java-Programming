class series1{
	public static void main (String[]args){
		int i=5;
		for(i=1;i<=5;i++){
			System.out.print("*");
			System.out.print(" ");
			for(int j=1;j<=i;j++){
				for (int k=1;k<=i-j;k++){
					System.out.print(" ");

					System.out.print("*");
				}
			for(i=1;i<=5;i++){
				System.out.print("*");
				System.out.print("");
			}
			}
		}
	}
}